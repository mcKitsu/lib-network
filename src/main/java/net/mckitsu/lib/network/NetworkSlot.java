package net.mckitsu.lib.network;

public class NetworkSlot {
    /* **************************************************************************************
     *  Variable <Public>
     */

    /* **************************************************************************************
     *  Variable <Protected>
     */

    /* **************************************************************************************
     *  Variable <Private>
     */
    private final NetworkSlotEvent networkSlotEvent;
    private boolean isOpen;

    /* **************************************************************************************
     *  Abstract method <Public>
     */

    /* **************************************************************************************
     *  Abstract method <Protected>
     */

    /* **************************************************************************************
     *  Construct Method
     */
    protected NetworkSlot(NetworkSlotEvent networkSlotEvent){
        this.networkSlotEvent = networkSlotEvent;
        this.isOpen = true;
    }

    /* **************************************************************************************
     *  Public Method
     */

    /*----------------------------------------
     *  close
     *----------------------------------------*/
    public void close(){
        if(this.isOpen){
            this.isOpen = false;
            this.networkSlotEvent.onClose(this);
        }
    }



    /* **************************************************************************************
     *  Public Method <Override>
     */

    /* **************************************************************************************
     *  Public Method <Static>
     */

    /* **************************************************************************************
     *  Protected Method
     */

    /* **************************************************************************************
     *  Protected Method <Override>
     */

    /* **************************************************************************************
     *  Protected Method <Static>
     */

    /* **************************************************************************************
     *  Private Method
     */

    /* **************************************************************************************
     *  Private Method <Override>
     */

    /* **************************************************************************************
     *  Private Method <Static>
     */
}
